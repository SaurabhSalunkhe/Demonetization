    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        BankToFederalBankWorkRequest bankToFederalBankWorkRequest = new BankToFederalBankWorkRequest();
        Date input = jCalendarComboBox1.getDate();
        LocalDate date = input.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        bankToFederalBankWorkRequest.setRequestDate(date);

        bankToFederalBankWorkRequest.setAmount(Integer.parseInt(txtExchangeAmmount.getText()));

        bankToFederalBankWorkRequest.setAmountToPickUp(Integer.parseInt(txtDepositAmmount.getText()));

        bankToFederalBankWorkRequest.setBankEnterprise((BankEnterprise) enterprise);

        for (Network network : business.getNetworkList()) {
            if (network.getEnterpriseDirectory().getEnterpriseList().contains(enterprise)) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof FederalOrganization) {
                            organization.getWorkQueue().getWorkRequestList().add(bankToFederalBankWorkRequest);
                            JOptionPane.showMessageDialog(null, "Request submitted succesfully", "Complete!", JOptionPane.PLAIN_MESSAGE);
                            bankToFederalBankWorkRequest.setStatus("Sent");
                            break;
                        }
                    }
                }
            }
        }

        ManagerWorkAreaJPanel managerWorkAreaJPanel = new ManagerWorkAreaJPanel(userProcessContainer, enterprise, business, userAccount);
        userProcessContainer.add("managerWorkAreaJPanel", managerWorkAreaJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_btnSubmitActionPerformed

