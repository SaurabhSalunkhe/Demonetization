  public void calculateTotalAmount() {
          Date input = jCalendarComboBox1.getDate();
        LocalDate selectedDate= input.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        System.out.println(selectedDate);
        
        int depositTotal=0; int exchangeTotal = 0;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof EmployeeOrganization) {
                for (WorkRequest workRequest : organization.getWorkQueue().getWorkRequestList()) {
                    if (workRequest.getStatus().equalsIgnoreCase("completed")) {
                        if(workRequest.isDeposit()==true&&workRequest.getRequestDate().equals(selectedDate)){
                        depositTotal = depositTotal + workRequest.getAmount();
                        }
                        if(workRequest.isDeposit()==false&&workRequest.getRequestDate().equals(selectedDate)){
                            exchangeTotal=exchangeTotal+workRequest.getAmount();
                        }
                        }
                }
            }
        }
        txtDepositAmmount.setText(String.valueOf(depositTotal));
        txtExchangeAmmount.setText(String.valueOf(exchangeTotal));
    }

